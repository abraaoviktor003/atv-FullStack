//Atribue√ßoes de chaveamentos - switch - case - break - default: nessa sequencia extritamente.
//exemplos
let turno = "manha"


switch (turno) {
    case "noite":
        alert ("boa noite")
        break;
    case "tarde":
        alert("boa tarde")
        break;
    case "manha":
        alert("bom dia")
        break;
    default:
        alert("turno desconhecido")
        break;
}
